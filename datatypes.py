# Python Data Types Demonstration

# Integer
a = 10
print("Integer value:", a)
print("Type:", type(a))

print("----------------------------")

# Float
b = 10.5
print("Float value:", b)
print("Type:", type(b))

print("----------------------------")

# String
c = "Hello Python"
print("String value:", c)
print("Type:", type(c))

print("----------------------------")

# Boolean
d = True
print("Boolean value:", d)
print("Type:", type(d))

print("----------------------------")

# List (ordered, changeable)
e = [1, 2, 3, 4]
print("List value:", e)
print("Type:", type(e))

print("----------------------------")

# Tuple (ordered, unchangeable)
f = (10, 20, 30)
print("Tuple value:", f)
print("Type:", type(f))

print("----------------------------")

# Set (unordered, unique items)
g = {10, 20, 30}
print("Set value:", g)
print("Type:", type(g))

print("----------------------------")

# Dictionary (keyâ€“value pairs)
h = {"name": "Alice", "age": 12}
print("Dictionary value:", h)
print("Type:", type(h))

print("----------------------------")

